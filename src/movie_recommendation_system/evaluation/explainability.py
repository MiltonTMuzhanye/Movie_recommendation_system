import pandas as pd
import numpy as np
from typing import Dict, List, Tuple, Any
import shap
from sklearn.ensemble import RandomForestRegressor
from src.movie_recommendation.utils.logger import logger

class ExplainabilityEngine:
    """
    Explainability engine for recommendation system
    """
    
    def __init__(self, config: Dict):
        """
        Initialize explainability engine
        
        Args:
            config: Configuration dictionary
        """
        self.config = config
        self.explainer = None
        
    def explain_recommendation(self, user_id: int, movie_id: int, 
                              features: pd.DataFrame, model) -> Dict[str, Any]:
        """
        Explain why a movie was recommended to a user
        
        Args:
            user_id: User ID
            movie_id: Movie ID
            features: Feature matrix
            model: Trained model
            
        Returns:
            Explanation dictionary
        """
        explanation = {
            'user_id': user_id,
            'movie_id': movie_id,
            'features': {},
            'contributions': {},
            'summary': ""
        }
        
        try:
            # Get feature importance
            if hasattr(model, 'feature_importances_'):
                importances = model.feature_importances_
                feature_names = features.columns
                
                # Get top features
                top_indices = np.argsort(importances)[::-1][:5]
                for idx in top_indices:
                    if importances[idx] > 0.05:
                        explanation['features'][feature_names[idx]] = float(importances[idx])
            
            # Generate summary
            if explanation['features']:
                top_feature = max(explanation['features'].items(), key=lambda x: x[1])
                explanation['summary'] = f"Movie recommended primarily due to {top_feature[0]}"
            else:
                explanation['summary'] = "Recommendation based on user preferences"
            
        except Exception as e:
            logger.warning(f"Could not generate explanation: {str(e)}")
            explanation['summary'] = "Recommendation generated by model"
        
        return explanation
    
    def explain_similar_movies(self, movie_id: int, 
                              similar_movies: List[Tuple[int, float]],
                              movies_df: pd.DataFrame) -> List[Dict[str, Any]]:
        """
        Explain why movies are similar
        
        Args:
            movie_id: Query movie ID
            similar_movies: List of similar movie IDs and scores
            movies_df: Movies dataframe
            
        Returns:
            List of explanation dictionaries
        """
        explanations = []
        
        query_movie = movies_df[movies_df['movieId'] == movie_id]
        if len(query_movie) == 0:
            return explanations
        
        query_genres = set(query_movie.iloc[0]['genres'].split('|'))
        
        for sim_movie_id, score in similar_movies:
            sim_movie = movies_df[movies_df['movieId'] == sim_movie_id]
            if len(sim_movie) == 0:
                continue
            
            sim_genres = set(sim_movie.iloc[0]['genres'].split('|'))
            common_genres = query_genres.intersection(sim_genres)
            
            explanation = {
                'movie_id': sim_movie_id,
                'similarity_score': score,
                'common_factors': []
            }
            
            if common_genres:
                explanation['common_factors'].append({
                    'factor': 'genres',
                    'value': list(common_genres)
                })
            
            # Add more explanations based on other features
            # This can be extended with more sophisticated analysis
            
            explanations.append(explanation)
        
        return explanations